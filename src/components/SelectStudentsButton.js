// src/components/evaluations/CreateEvaluationButton.js
import React, { PureComponent } from 'react'
import PropTypes from 'prop-types'
import { connect } from 'react-redux'
import RaisedButton from 'material-ui/RaisedButton'
import StarIcon from 'material-ui/svg-icons/action/favorite'
import createEvaluation from '../../actions/evaluations/create'

class SelectStudentsButton extends PureComponent {
  static propTypes = {
    signedIn: PropTypes.bool,
  }

  render() {
    if (!this.props.signedIn) return null

    return (
      <div className="CreateEvaluationButton">
        <RaisedButton
          label="Create Evaluation"
          primary={true}
          onClick={this.props.createEvaluation}
          icon={<StarIcon />} />
      </div>
    )
  }
}

const mapStateToProps = ({ currentUser }) => ({
  signedIn: !!currentUser && !!currentUser._id,
})

export default connect(mapStateToProps, { createEvaluation })(SelectStudentsButton)
